    public function welcome(): void
    {
        info('Welcome to Moox BackupServerUi Installer');
    }

    public function publish_configuration(): void
    {
        if (confirm('Do you wish to publish the configuration?', true)) {
            info('Publishing BackupServerUi Configuration...');
            $this->callSilent('vendor:publish', ['--tag' => 'backup-server-ui-config']);
        }
    }

    public function register_plugins(): void
    {
        note('Registering the Filament Resources...');

        $providerPath = $this->getPanelProviderPath();

        if (File::exists($providerPath)) {
            $content = File::get($providerPath);

            $intend = '                ';

            $namespace = "\Moox\BackupServerUi";

            $pluginsToAdd = multiselect(
                label: 'These plugins will be installed:',
                options: ['BackupLogItemPlugin', 'BackupPlugin', 'DestinationPlugin', 'SourcePlugin'],
                default: ['BackupLogItemPlugin', 'BackupPlugin', 'DestinationPlugin', 'SourcePlugin'],
            );

            $function = '::make(),';

            $pattern = '/->plugins\(\[([\s\S]*?)\]\);/';
            $newPlugins = '';

            foreach ($pluginsToAdd as $plugin) {
                $searchPlugin = '/'.$plugin.'/';
                if (preg_match($searchPlugin, $content)) {
                    info("$plugin already registered.");
                } else {
                    $newPlugins .= $intend.$namespace.'\\'.$plugin.$function."\n";
                }
            }

            if ($newPlugins) {
                if (preg_match($pattern, $content)) {
                    info('Plugins section found. Adding new plugins...');

                    $replacement = "->plugins([$1\n$newPlugins\n            ]);";
                    $newContent = preg_replace($pattern, $replacement, $content);
                } else {
                    info('Plugins section created. Adding new plugins...');

                    $pluginsSection = "            ->plugins([\n$newPlugins\n            ]);";
                    $placeholderPattern = '/(\->authMiddleware\(\[.*?\]\))\s*\;/s';
                    $replacement = "$1\n".$pluginsSection;
                    $newContent = preg_replace($placeholderPattern, $replacement, $content, 1);
                }

                File::put($providerPath, $newContent);
            }
        } else {
            alert('AdminPanelProvider not found. You need to add the plugins manually.');
        }
    }

    public function getPanelProviderPath(): string
    {
        $providerPath = app_path('Providers/Filament');
        $providers = File::allFiles($providerPath);
        if (count($providers) > 1) {
            $providerNames = [];
            foreach ($providers as $provider) {
                $providerNames[] = $provider->getBasename();
            }
            $providerPath .= '/'.$this->choice(
                'Which Panel should it be registered',
                [...$providerNames]
            );
        }
        if (count($providers) == 1) {
            $providerPath .= '/'.$providers[0]->getBasename();
        }

        return $providerPath;
    }

    public function finish(): void
    {
        note('Moox BackupServerUi installed successfully. Enjoy!');
    }
